    private void Login() {
        if (!txt_usuario.getText().isEmpty() && !txt_password.getText().isEmpty()) {
        String usuarioIngresado = txt_usuario.getText().trim();
        String passwordIngresado = txt_password.getText().trim();

        // Verificar credenciales para el administrador
        if (usuarioIngresado.equals("JCP") && passwordIngresado.equals("123")) {
            FrmMenu menuAdmin = new FrmMenu();
            menuAdmin.setVisible(true);
            this.dispose();
        } 
        // Credenciales para otros usuarios
        else {
            Ctrl_Usuario controlUsuario = new Ctrl_Usuario();
            Usuario usuario = new Usuario();
            usuario.setUsuario(usuarioIngresado);
            usuario.setPassword(passwordIngresado);

            if (controlUsuario.loginUser(usuario)) {
                FrmMenu menu = new FrmMenu();
                menu.setVisible(true);
                this.dispose();
            } else {
                JOptionPane.showMessageDialog(null, "Usuario o Clave Incorrectos");
            }
        }
    } else {
        JOptionPane.showMessageDialog(null, "Ingrese sus credenciales");
    }
    }
